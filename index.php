			$('#enviarNews').click(function (e) {
				e.preventDefault();
				validaForm($('#contactNews'));
				if (validaForm($('#contactNews')) == true) {
					
					//$('#contactNews').submit();
					var parametros = {};
					parametros.news = $('[name="news"]').val();
					parametros.resp = 'valido';
					$.ajax({
							url: 'news.php',
							type: 'post',
							dataType:'text',
							data: parametros,
							beforeSend: function(){
								$('.news-mes').addClass('hide');
								$('#enviarNews').addClass('hide').after('<span class="btn btn-secondary btn-lg newsBtn" id="loadding">Enviando... <i class="fa  fa-spinner fa-spin"></i></span>');
							},
							success: function (data) {
								var json = JSON.parse(data);
								$('.news-mes').text(json.Mensagem);
								$('.news-mes').removeClass('hide');
								$("html, body").animate({
									scrollTop: $('.news-mes').offset().top - 110
								}, {
									duration: 500,
									easing: "swing"
								});
								$('#enviarNews').removeClass('hide');
								$('#loadding').remove();
							},
							error: function(result) {
								alert("Data not found");
							}
						});
				}
			});
